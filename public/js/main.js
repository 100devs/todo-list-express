// declaring variable for delete item
const deleteBtn = document.querySelectorAll('.fa-trash')
// declaring variable for list item
const item = document.querySelectorAll('.item span')
// declaring variable for completed item
const itemCompleted = document.querySelectorAll('.item span.completed')

// Loops to create event listeners for each element for delete, complete, and uncomplete
Array.from(deleteBtn).forEach((element)=>{
    element.addEventListener('click', deleteItem)
})

Array.from(item).forEach((element)=>{
    element.addEventListener('click', markComplete)
})

Array.from(itemCompleted).forEach((element)=>{
    element.addEventListener('click', markUnComplete)
})

// Create an async function
async function deleteItem(){
    // declaring a variable to the list item in the DOM
    const itemText = this.parentNode.childNodes[1].innerText
    try{
        // declaring a variable to get a delete response from the server (deleteItem method)
        const response = await fetch('deleteItem', {
            method: 'delete',
            headers: {'Content-Type': 'application/json'},
            body: JSON.stringify({
              'itemFromJS': itemText
            })
          })
        const data = await response.json()
        console.log(data)
        location.reload()

    }catch(err){
        console.log(err)
    }
}

