const deleteBtn = document.querySelectorAll('.fa-trash')
const item = document.querySelectorAll('.item span')
const itemCompleted = document.querySelectorAll('.item span.completed')

Array.from(deleteBtn).forEach((element)=>{
    element.addEventListener('click', deleteItem)
})

Array.from(item).forEach((element)=>{
    element.addEventListener('click', markComplete)
})

Array.from(itemCompleted).forEach((element)=>{
    element.addEventListener('click', markUnComplete)
})

async function deleteItem(){
  // 'this' is the trashcan, aprentNode is the li, childNodes[1] is the span
    const itemText = this.parentNode.childNodes[1].innerText
    try{
      // route of deleteItem, send DELETE request, for itemText
        const response = await fetch('deleteItem', {
            method: 'delete',
            headers: {'Content-Type': 'application/json'},
            body: JSON.stringify({
              'itemFromJS': itemText
            })
          })
        const data = await response.json()
        console.log(data)
        location.reload()

    }catch(err){
        console.log(err)
    }
}

async function markComplete(){
  // Grabs text of the span, which will be sent along the req.body as property itemFromJS
  // We clicked on the span. The parentnode of the span is the li. childNodes[1] is the span (childNodes[0] is the bullet point)
    const itemText = this.parentNode.childNodes[1].innerText
    try{
      // PUT request on route markComplete
        const response = await fetch('markComplete', {
            method: 'put',
            headers: {'Content-Type': 'application/json'},
            body: JSON.stringify({
                'itemFromJS': itemText
            })
          })
        const data = await response.json()
      // console.log the response to client side
        console.log(data)
      // Refresh to make trigger a GET request
        location.reload()

    }catch(err){
        console.log(err)
    }
}

